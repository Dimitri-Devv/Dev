package fr.dawan.d.operateurs;

public class Arithmétiques {

    public static void main(String[] args) {
        
        int v1 = 10;
        int v2 = 4;
        
        System.out.println("v1 + v2 = " + (v1 + v2)); // Addition
        System.out.println("v1 - v2 = " + (v1 - v2)); // Soustraction
        System.out.println("v1 * v2 = " + (v1 * v2)); // Multiplication
        System.out.println("v1 / v2 = " + (v1 / v2)); // Division
        System.out.println("v1 % v2 = " + (v1 % v2)); // Modulo : Reste de la division entière
                                                      // Exmple : 10 / 4 = 2 et il reste 2 => 10 % 4 = 2
    }

}
