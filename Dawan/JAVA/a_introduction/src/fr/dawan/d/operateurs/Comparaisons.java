package fr.dawan.d.operateurs;

public class Comparaisons {

public static void main(String[] args) {
        
        int a = 1, b = 2;
        
        System.out.println("a == b" + (a == b)); // égalité
        System.out.println("a != b" + (a != b)); // inégalité
        System.out.println("a > b" + (a > b)); // supérieur
        System.out.println("a < b" + (a < b)); // inférieur
        System.out.println("a >= b" + (a >= b)); // supérieur ou égal
        System.out.println("a <= b" + (a <= b)); // inférieur ou égal

    }

}
